print("Math Functions")

# absolute value function
abs_int = abs(-1)
print(abs_int)

# float function will turn any number into a floating point value
int_to_float = float(100)
print(int_to_float)

# turns any float into an integer
print(int(1.23))

print(max(1, 2, -5, 10, 0))
print(min(1, 2, -5, 10, 0))
print(pow(2, 3))  # power function
print(round(51.6))